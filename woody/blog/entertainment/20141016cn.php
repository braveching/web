<h3><a name="SZ162411">华宝油气</a>和XOP套利交易规则</h3>
<p>2015年8月15日
<br />分级A的交易规则定完后就再也没有碰到买入的机会. 大家都说7月8号那种分级A集体跌停的机会百年难遇, 看来我这辈子再也碰不到了. 
<br />炒股的好处不只限于分级A, 其实这一年来给我印象最深的是看<?php EchoXueqieId('3037882447', '云蒙'); ?>来回在港股和A股之间买卖招商银行的股票, 哪边低就持有哪个. 用不到2百万的本金, 1年交易了几个亿, 稳稳赚了几百万.
这种锚定一个标的来回交易套利的模式值得学习. 
<br />Qualcomm收购CSR的现金下周终于有希望能到账了. 本来宣布的是6月底, 结果中国政府一直拖到快7月底才批准这个收购. 美国/英国/韩国/日本/印度都在此之前批准了, 而可爱的台湾一开始就干脆放弃了这项权利. 
由于我的美股交易依旧毫无章法, 我其实有点发愁怎么处理这笔钱. 最省心的就是直接都买成Qualcomm股票, 然后接着放着不去管它.
反复考虑后, 我还是决定效仿云蒙. 锚定标普油气开采指数^SPSIOP的2个基金, 用我低交易费用的国联证券账户, 在A股的华宝油气(<?php EchoMyStockLink('SZ162411'); ?>)和美股的<?php EchoMyStockLink('XOP'); ?>之间来回交易套利.
建立简单规则如下:
<ol>
  <li><b>NYMEX原油这一轮价格在60美元以下时执行此规则.</b> 去年底我开始抄底原油和相关股票的ETF, 碰到的还是老问题, 抄底的时候总抄在半山腰, 涨一点就忍不住卖光了. 希望通过套利交易能够克服我的持仓恐惧症.
  <li><b>折价和溢价计算按照已经发生的原则.</b> 我会根据每天A股开盘前官方发布的<a href="../../res/sz162411cn.php">华宝油气净值</a>调整软件的计算公式参数.
  <li><b>华宝油气价格在0.50以下时, 每次交易5万股. 价格在0.50到0.67之间时, 每次交易4万股. 价格在0.67到1.00之间时, 每次交易3万股.</b> 满足最低5块的交易费用.
  <li><b>华宝油气每累计交易16万股时, 对应XOP一次交易400股.</b> 原则上还是不做大交易, 不过兼顾把固定费率交易的美股平均费用比例拉低到国联证券同一个数量级水平.
  <li><b>华宝油气溢价超过1.5%时分批每次提高0.1分卖出, 在XOP不高于上个收盘价的条件下选择一个短期均线值买回相应份额</b>. 1.5%相当于华宝油气申购费率.
  <li><b>华宝油气折价超过0.5%时分批每次降低0.1分买入, 在XOP不低于上个收盘价的条件下选择一个短期均线值卖出相应份额</b>. 0.5%相当于华宝油气赎回费率.
</ol>
<br />华宝油气虽然跟踪的是美国的页岩气公司股票, 但是它喜欢跟A股涨跌, 而且涨的时候往往会溢价, 而跌的时候总会折价. 开始我一直觉得是A股钱多人傻, 买卖的人根本不知道自己买卖的是什么标的. 
但是后来我进入了第二层境界考虑这个问题, 由于A股的涨跌直接影响大宗商品的价格, 华宝油气跟A股短期走势一致也是可以理解的.  
今天老孙在微信上炫耀他读高中的儿子这个暑假2个月用18万本金在<a href="#A">分级基金</a>上套利总共获利2.5万人民币. 我于是进入了<font color=lime>看山还是山看水还是水</font>的第三层境界: A股市场钱多人傻, 可能的确是套利交易者生存的好地方.
</p>

