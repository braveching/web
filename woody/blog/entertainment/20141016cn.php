<h3>华宝油气和XOP套利更新</h3>
<p>2016年7月16日
<br />华宝油气和XOP套利搞了差不多一年, 现在的操作跟一年前已经有很大的不同. 好记性不如烂笔头, 记录一下这期间的变化和目前的状态. 
<br />交易费用下降了, A股新开了万分之2佣金的华泰证券账户, 最近客户经理还把我的基金交易佣金又调低到了万分之一, 而且它家基金交易还没有最低5块的限制.
