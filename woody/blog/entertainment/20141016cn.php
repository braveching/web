<br />总交易金额迅速增加, 多到我不胜其烦, 干脆结束了原来用Excel统计交易量的栏目. 
<br />花在交易上的时间也是急剧增加, 已经让我相当怀疑这么做有没有必要了, 还是要尽可能简化交易模式, 避免盯盘. 另外要赶快把IB的API搞起来, 自动交易美股部分. 
<br />更新华宝油气和XOP套利规则如下:
<ol>
  <li>华宝油气每次交易8万股, 对应XOP 200股.
  <li>XOP全部采用<a href="20150818cn.php#sma">均线</a>价格交易, 不做盘前盘后交易.
  <li>华宝油气溢价时, 在最接近的XOP均线上开始分批每次提高0.1分卖出.
  <li>华宝油气折价时, 在最接近的XOP均线上开始分批每次降低0.1分买入.
  <li>赔钱的买卖坚决不做, 每笔华宝油气或者XOP的交易都要保证是基于以前的某笔买卖赚钱的, 否则坚决不交易.
</ol>
<br />总体来说, 现在华宝油气的交易者群体精明了很多. 跟A股无由头涨跌的时候越来越少, 更多的是跟白天<a href="../../res/clcn.php">美油期货CL</a>价格波动. 也许不应该把我现在这个操作称为套利了. 
