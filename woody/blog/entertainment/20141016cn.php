<h1>大道至简从上证大型国有股获利</h1>
<p>2014年10月16日
<br />昨天是我的幸运日. Qualcomm同意现金收购<a href="20090219cn.php">CSR</a>的新闻让我这唯一的英国股票上涨了30%. 而中国国航(<?php EchoMyStockLink('SH601111'); ?>), 目前我唯一重仓的A股, 在昨天拉了个涨停.
<br />我从2000年开始在美国市场上买卖股票, 一直都是一个失败的<a href="20111112cn.php">投资</a>. 我总是在股票刚开始涨的时候就很快卖掉, 然后看它一路往上涨很久, 而会把严重亏损的股票一直持有很多年.
今年6月<b>自愿</b>放弃我的美国绿卡后, 因为没有了交税的心理负担, 我一直在考虑卖掉最近几年上涨了很多的CSR股票. 但是过去的经历让我总是觉得这很可能又会是另外一个错误的大决定. 最后, 感谢Qualcomm替我做出了选择.
<ol>
  <li>2012年我开了A股账户. 跟美国单一笔的交易费用不同, 中国的券商按照交易金额比例收费. 比如我开户的<a href="http://bocichina.com" target=_blank>中银国际</a>收0.075%, 最低5块.
      对我来说这是个好的开始, 从此我改变了交易风格, 停止大笔交易, 定下了第一条交易规则:
      <br /><b>只做小交易. 6.66元以上的股票每次交易1000股. 价格在3.33到6.66之间的每次交易2000股. 2.22到3.33之间的每次3000股.</b>
  <li>2000年的时候我有一阵子天天都狂买进卖出. 结果不光赔了钱, 第2年报税的时候又被折磨了一遍, 一条条的输入每个卖出记录. 后来我就学会了更加耐心的交易.
      美国市场上我可以下个单然后3个月不去管它, 不过国内只能当天有效. 依此定第2条:
      <br /><b>每天刚过午夜后下单, 白天交易时段不再更改.
      根据每日更新的<a href="http://vip.stock.finance.sina.com.cn/q/go.php/vDYData/kind/znzd/index.phtml" target=_blank>新浪</a>数据, 在支持位价格下买单, 在阻力位下卖单.</b> 
  <li>因为这么多年来都没有培养成功我的止损习惯, 我决定只操作A股的大型国企股票, 这样市场不好的时候我一样可以安心持有. 我用上证指数指导第3条规则:
      <br /><b>上证指数每上升100点卖出5万股票. 因为我目前处于逐步建仓阶段, 每下降100点增仓5-10万.</b>
  <li>我用第4条规则锁定利润:
      <br /><b>对单独某个股票, 当上证指数(规则3)上涨的时候, 如果阻力位(规则2)的价格比最后一次买入的价格高5%就下卖单(规则1), 当阻力位不变或者上升时持续卖出, 直到总仓位达到规则3的标准.
      反之当上证指数下降的时候, 如果压力位的价格比最后一次卖出的价格低5%就下买单, 当压力位不变或者下降时持续买入, 直到总仓位达到规则3的标准.</b>
</ol>
<br />基于以上规则, 我希望能带来平均每年10%的收益, 但愿未来能够成功.  
</p>
