<h3><a name="A">分级A交易规则</a></h3>
<p>2015年7月12日
<br />伴随着全民借钱炒股的疯狂, 最近几个月来对分级基金的讨论日益火爆. 各种申购/赎回/上折/下折套利让人眼花缭乱, 我觉得麻烦, 一直没有特别关注. 
<br />一个多月前上证指数站上5000点那天后跟老孙的几个朋友吃饭, 席间谈论的当然都是必破6124点的股票市场. 老孙听说我绝大部分人民币都放在银行买收益率5%的理财产品后, 大力推荐我购买当时折价已经在20%以上的分级A基金.
<br />基于对老孙学习精神的信任, 我赶紧恶补了一下. 发现折价20%以上的分级A的确是个好东西, 收益率比银行理财高, 风险却比银行理财低, 显然过去三年中我低估了人民币的无风险收益.
<br />考虑到分级A操作会比我的大型国企股票要频繁得多, 我在<a href="http://www.glsc.com.cn" target=_blank>国联证券</a>网上开了个0.025%, 最低5块交易费用的新帐户. 
本来是想联系中银国际降低佣金的, 无奈股市太火爆, 电话永远打不进去, 根本联系不上. 
<br />然后开始逐步杀入, 一个月简单买入卖出操作下来, 在分级A上的钱居然已经赚了相当于1年多的利息. 高兴之余今天总结一个大道至简的分级A交易规则:
<ol>
  <li>不参加母基金的申购, 也尽可能不参加母基金的赎回, 而是在市场上直接买卖. 同时尽量不参加上折和下折. 
  <li>根据当日分级A行情, 圈定所有折价20%以上的, 选择尚未持有的品种, 按照折价最多的依次买入. 每个品种每次3万股, 总额大约24000左右, 满足最低5块的交易费用. 
  <li>分级A上升时, 每笔买入盈利超过5%以上的时候卖出. 返回上一步, 直到所有折价20%以上的品种都已经持有为止.
  <li>分级A下跌时, 单个品种每下跌2%以上的时候补仓3万股. 
  <li>最小交易价格单位为1分钱, 不下0.795这种单.
</ol>  
<br />今年以来上证指数大幅波动, 按照它调整仓位成了个笑话, 调仓规则和公式早已经统统弃用. 中国打头的股票在持续不断的上升过程中几乎已经卖光.
但是因为仓位太低, 靠国企股票估计难维持去年整体21%的收益率, 希望分级A能够助一臂之力. 
<br />因为分级A可能会替代掉银行理财的地位, 去掉100万的上限资金限制, 看在我的交易规则下能够最多买到多少.
</p>
