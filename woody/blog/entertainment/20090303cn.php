<h1>另外一个人的十年</h1>
<p>2009年3月3日
<br />当一个人回顾过去的时候, 就说明他老了. 
<br />我坐在他面前, 并没有发现这些年他有什么变化, 还是很瘦很精神的样子. 我第一次见到他差不多是12年前, 这些年彼此来往极少.
他还在按照他印象中的我跟别人介绍<font color=grey>这个人说他什么都能做</font>, 却不知道近几年我最常说的是<font color=grey>这个东西我不会</font>. 
<br />1999年, 当<a href="../palmmicro/20061123cn.php">王老板</a>招我们这些刚毕业的硕士开<a href="../palmmicro/20080326cn.php">Palmmicro</a>公司的时候, 曾经想请他来管理我们.
当时开过几次会, 比较有印象的是他一直积极要求我们找市场上现有的同类产品来定义我们要做的东西. 后来他并没有跟随王老板, 而是作为难得的海归人才进了一家民营家电企业.
<br />三年合同期满, 海龟游回美国, 在一家公司打工半年. 在2003年间见了一次面, 我给他家里装了一个刚刚开始成型的<a href="../../../pa1688/indexcn.html">PA1688</a>网络电话.
后来听说他很快又回国了, 跟我们都认识的一些人在上海做. 
<br />2006年, 我在深圳意外碰到一个以前上海Centrality的熟人, 得知他在深圳做GPS产品, 请这个人在帮他做软件. 随后要了他的联系方式, 跟他通了电话.
<br />昨天见到他, 知道了更多的情况. 他在三个月前刚刚结束了跟一家上市公司另外一个三年的合同, 现在在自己开公司做产品设计. 当年的GPS产品因为技不如人没有做成功.
很多年没有再回美国, 他的绿卡早已经被移民局收走. 他说<font color=grey>一旦回来后就再也不可能回去了</font>. 
<br />我没有问他这十年的感受, 不过我想体会大体是差不多的. 虽然这十年我们做得不算成功, 但是并不后悔每一步的选择. 
