<h1>Ten Years of Another Man</h1>
<p>March 3, 2009
<br />Only old people like to talk about history.
<br />I sat in front of him, did not find much changes on him. He is still thin and full of energy. It was 12 years ago when I first met him, and we had not made much contact all those years.
He still introduces me to others according to his old memory <font color=grey>This guy says he can do everything</font>.
But he did not know what I mostly said in recent years was <font color=grey>I do not know how to do this</font>.
<br />In 1999, when <a href="../palmmicro/20061123.php">Dr Wang</a> hired those new graduates including myself to start <a href="../palmmicro/20080326.php">Palmmicro</a>,
Dr Wang was hoping he could join too and manage us. We had a few meetings at that time, still had the impression that he insisted instructing us to find similar products on market to define our own plan.
But he did not join Dr Wang, instead, he joined a local Chinese consumer electric company as a precious leading role back from abroad.
<br />After the 3 years contract ended, he went back to US, spent half of a year working in a company. I met his once in that 2003 period,
and installed a newly designed <a href="../../../pa1688/index.html">PA1688</a> IP phone in his home.
Later I heard that he went back to China again and working in Shanghai with some our old acquaintance.
<br />In 2006, I met a Shanghai Centrality people in Shenzhen by chance, and heard that he was designing GPS products in Shenzhen. I got his contract information and called him.
<br />I met him yesterday and got to know more. He ended another 3 year contract with a public company 3 months ago. Now he is running his own design company.
The GPS product in 2006 was not successful. Did not go back to US for many years, his green card was cancelled by immigration already. He said <font color=grey>Once you come here you can never go back again</font>.
<br />I did not ask him how about the 10 years in China, but I guess we feel the same. Although it is far from successful, I never regretted.
